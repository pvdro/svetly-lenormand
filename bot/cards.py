assert len(DECK) == 36
_BY_NUM = {c.number: c for c in DECK}


def get_card(number: int) -> Card:
    return _BY_NUM[number]


def draw(n: int = 1, *, seed: int | None = None) -> list[Card]:
    """Случайные карты без повторов."""
    rng = random.Random(seed)
    return rng.sample(DECK, k=min(n, len(DECK)))


def card_title(card: Card) -> str:
    return f"{card.emoji} **{card.number}. {card.name}**"


def format_card_short(card: Card) -> str:
    return f"{card.emoji} {card.number}. {card.name} — _{card.keywords}_"


def format_card_full(card: Card, focus: str = "general") -> str:
    body = {
        "general": card.general,
        "love": card.love,
        "work": card.work,
        "advice": card.advice,
    }.get(focus, card.general)
    return (
        f"{card_title(card)}\n"
        f"_{card.keywords}_\n\n"
        f"{body}\n\n"
        f"💫 Совет: {card.advice}"
    )

@dataclass(frozen=True)
class TarotCard:
    id: str
    name: str
    emoji: str
    arcana: str  # major | wands | cups | swords | pentacles
    number: int
    keywords: str
    upright: str
    advice: str


def _m(n, name, emoji, kw, upright, advice) -> TarotCard:
    return TarotCard(f"m{n}", name, emoji, "major", n, kw, upright, advice)


def _p(suit: str, n: int, name: str, emoji: str, kw: str, upright: str, advice: str) -> TarotCard:
    return TarotCard(f"{suit[0]}{n}", name, emoji, suit, n, kw, upright, advice)


